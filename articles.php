According to the National Eating Disorders Association, intuitive eating involves not criticizing yourself or being influenced by diet culture 
but rather trusting your body to choose foods that feel good for you.<sup>[1]</sup>  It means eating when you feel hungry 
and listening to your body and your intuitiveness in eating 
